from __future__ import annotations
import json
from pathlib import Path
from typing import Any
from fastapi import FastAPI, HTTPException
from fastapi.middleware.cors import CORSMiddleware
from src.pipeline.config import DASHBOARD_JSON_FILENAME

import os

DATA_FILE = Path(DASHBOARD_JSON_FILENAME)

_DATA_CACHE: dict[str, Any] = {}
_LAST_MODIFIED: float = 0.0


def get_data() -> dict[str, Any]:
    global _DATA_CACHE, _LAST_MODIFIED
    if not DATA_FILE.exists():
        return {}
    try:
        mtime = os.path.getmtime(DATA_FILE)
        if mtime > _LAST_MODIFIED:
            with DATA_FILE.open(encoding="utf-8") as f:
                _DATA_CACHE = json.load(f)
            _LAST_MODIFIED = mtime
    except Exception:
        pass
    return _DATA_CACHE


app = FastAPI(
    title="SalesIQ - E-Commerce Sales Intelligence API",
    description=(
        "REST endpoints that expose the cleaned ETL data, product summaries, "
        "regional stats, fraud orders, inventory priority, and AI insights "
        "generated by run_pipeline.py."
    ),
    version="1.0.0",
)

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_methods=["*"],
    allow_headers=["*"],
)


def _require(key: str) -> Any:
    """Return DATA[key] or raise 404 if data not loaded."""
    data = get_data()
    if not data:
        raise HTTPException(
            status_code=503,
            detail="dashboard_data.json not found — run run_pipeline.py first.",
        )
    if key not in data:
        raise HTTPException(
            status_code=404, detail=f"Key '{key}' not in data.")
    return data[key]


# -- Health --
@app.get("/", tags=["Health"])
def root():
    """Ping endpoint — verifies the server is reachable."""
    data = get_data()
    return {
        "status": "ok",
        "message": "SalesIQ API is running. Visit /docs for Swagger UI.",
        "data_loaded": bool(data),
    }


@app.get("/health", tags=["Health"])
def health():
    """Returns server health and whether dashboard data is loaded."""
    data = get_data()
    return {
        "healthy": True,
        "data_file": str(DATA_FILE),
        "data_loaded": bool(data),
        "total_orders": data.get("meta", {}).get("total_orders", 0) if data else 0,
    }


