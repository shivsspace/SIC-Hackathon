# -- Orders --
@app.get("/orders", tags=["Orders"])
def get_orders(limit: int = 50, offset: int = 0):
    """
    Paginated list of all cleaned orders.
    """
    orders = _require("orders")
    return {
        "total": len(orders),
        "offset": offset,
        "limit": limit,
        "orders": orders[offset: offset + limit],
    }


